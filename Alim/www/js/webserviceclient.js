function getReference(surahNumber,authorNameIndex){
    var author_code = "";
    if(authorNameIndex=="Asad"){
        author_code="ASD";
    } if(authorNameIndex=="Malik"){
        author_code ="MAL";
    } if(authorNameIndex=="YusufAli"){
        author_code="YAT"	;
    } if(authorNameIndex=="Pickthall"){
        author_code="PIK";
    }
    var url = "http://www.alim.citrusdev.com/library/quran/surah/english/"+surahNumber+"/"+author_code+"/data.json";
   
    showLoading();
        $('#authorReference').html("<br/><ul id='newList' data-inset='false' data-theme='d' data-dividertheme='b'></ul>");
    $("#newList").listview();
    $.getJSON(url,function(data) {
             
              for(i=0;i<(data.nodes.length)-1;i++){
              
              $("#newList").append("<li class='ui-li-desc'><div><B>"+data.nodes[i].node.field_ayah_no_value_1+":</B></div><small><div class='ui-li-desc'>"+data.nodes[i].node.body+"</div></small></li>");
              $("#newList").listview("refresh");
              
              }
              
              })
    .success(function() {hideLoading();})
    .error(function() { 
           hideLoading();})
    .complete(function() {hideLoading(); });
    
}
     